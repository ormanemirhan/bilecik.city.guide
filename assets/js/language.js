  const switcher = document.querySelector('.lang-switch');
  const btn = document.getElementById('current-lang');
  const menu = document.getElementById('lang-menu');
  
  // Açılır menüyü toggle et
  btn.addEventListener('click', () => {
    menu.style.display = menu.style.display === 'block' ? 'none' : 'block';
  });
  document.addEventListener('click', e => {
    if (!switcher.contains(e.target)) {
      menu.style.display = 'none';
    }
  });




  
  // Dil seçildiğinde:
  menu.querySelectorAll('li').forEach(li => {
    li.addEventListener('click', () => {
      const newLang = li.getAttribute('data-lang');
      // Butonu güncelle
      const img = li.querySelector('img').getAttribute('src');
      btn.querySelector('img').src = img;
      btn.childNodes[1].nodeValue = ' ' + newLang.toUpperCase() + ' ▼';
      menu.style.display = 'none';
  
      // Sayfadaki tüm data-key öğelerini çevir
      document.querySelectorAll('[data-key]').forEach(el => {
        const key = el.getAttribute('data-key');
        el.textContent = translations[newLang][key];
      });
    });
  });

  // Sayfa Yüklendiğinde Tarayıcı Diline Göre Seç

  window.addEventListener('DOMContentLoaded', () => {
    const lang = navigator.language;
    if (lang.startsWith('de')) applyLanguage(de);
    else if (lang.startsWith('ar')) applyLanguage(ar);
    else if (lang.startsWith('en')) applyLanguage(en);
    else if (lang.startsWith('es')) applyLanguage(es);
    else if (lang.startsWith('ru')) applyLanguage(ru);
    else if (lang.startsWith('ja')) applyLanguage(ja);
    else if (lang.startsWith('fr')) applyLanguage(fr);
    else if (lang.startsWith('pt')) applyLanguage(pt);
    else applyLanguage(tr);
  });
  